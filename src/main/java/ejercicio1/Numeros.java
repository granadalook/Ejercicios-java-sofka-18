package ejercicio1;

public class Numeros {
    public static void main(String[] args) {
        int numeroA = 10;
        int numeroB = 12;
        if (numeroA == numeroB) {
            System.out.println("Los numeros son iguales");
        }
        if (numeroA < numeroB) {
            System.out.println("El numero " + numeroB + " es meyor que el numero " + numeroA);
        }
        if (numeroA > numeroB) {
            System.out.println("El numero " + numeroA + " es mayor que el numero " + numeroB);
        }
    }
}
